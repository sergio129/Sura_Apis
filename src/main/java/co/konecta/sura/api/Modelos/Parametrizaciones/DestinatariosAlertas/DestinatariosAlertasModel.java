package co.konecta.sura.api.Modelos.Parametrizaciones.DestinatariosAlertas;

public class DestinatariosAlertasModel {
    private String departments;
    private String emails;
    private String lines;
    private String municipalies;
    private String par_name;
    private String services;
    private String token;

    public String getDepartments() {
        return departments;
    }

    public void setDepartments(String departments) {
        this.departments = departments;
    }

    public String getEmails() {
        return emails;
    }

    public void setEmails(String emails) {
        this.emails = emails;
    }

    public String getLines() {
        return lines;
    }

    public void setLines(String lines) {
        this.lines = lines;
    }

    public String getMunicipalies() {
        return municipalies;
    }

    public void setMunicipalies(String municipalies) {
        this.municipalies = municipalies;
    }

    public String getPar_name() {
        return par_name;
    }

    public void setPar_name(String par_name) {
        this.par_name = par_name;
    }

    public String getServices() {
        return services;
    }

    public void setServices(String services) {
        this.services = services;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
