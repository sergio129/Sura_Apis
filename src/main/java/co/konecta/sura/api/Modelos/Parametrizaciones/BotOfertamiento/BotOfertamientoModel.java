package co.konecta.sura.api.Modelos.Parametrizaciones.BotOfertamiento;

public class BotOfertamientoModel {
    private String coordination_manager;
    private String departments;
    private String lines;
    private String municipalities;
    private String name;
    private String offert_type;
    private String service_type;
    private String time;
    private String token;


    public String getCoordination_manager() {
        return coordination_manager;
    }

    public void setCoordination_manager(String coordination_manager) {
        this.coordination_manager = coordination_manager;
    }

    public String getDepartments() {
        return departments;
    }

    public void setDepartments(String departments) {
        this.departments = departments;
    }

    public String getLines() {
        return lines;
    }

    public void setLines(String lines) {
        this.lines = lines;
    }

    public String getMunicipalities() {
        return municipalities;
    }

    public void setMunicipalities(String municipalities) {
        this.municipalities = municipalities;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOffert_type() {
        return offert_type;
    }

    public void setOffert_type(String offert_type) {
        this.offert_type = offert_type;
    }

    public String getService_type() {
        return service_type;
    }

    public void setService_type(String service_type) {
        this.service_type = service_type;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
